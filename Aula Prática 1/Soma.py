#Este código faz a soma, multiplicação, subtração e divisão entre dois números

numero1 = 12 #primeira variável número 1
numero2 = 14 #primeira variável número 2

soma = numero1 + numero2 #criação de variável de soma 
multip = numero1 * numero2 #criação de variável de multiplicação
sub = numero1 - numero2 #criação de variável de subtração
divi = numero1 / numero2 #criação de variável de divisão

print (f"a soma de {numero1} + {numero2} é {soma}")
print (f"a multiplicação de {numero1} * {numero2} é {multip}")
print (f"a subtração de {numero1} - {numero2} é {sub}")
print (f"a divisão de {numero1} / {numero2} é {divi}")
print("A soma de {} + {} é: {}".format(numero1, numero2, soma))