#Este código faz o cálculo de volume utilizando valores de comprimento, largura e área.

comprimento = 12 #variável 1
largura = 14 #variável 2
altura = 20 #variável 3

volume = comprimento * largura * altura #Cálculo para volume em cm³

#Resultado detalhado da conta feita
print(f"o volume da caixa é a multipliacação de comprimento ({comprimento}) , largura ({largura}) e altura ({altura}), resultando em seu volume ({volume}) em cm³" )